package instructions;

import expressions.Expression;
import semantic.EnumTag;
import types.Type;

public class DeclarationInstruction extends Instruction {
		
	public DeclarationInstruction( Expression e2, Type e1) {
		super(e2,e1, EnumTag.DECL);
	}
	
	public String toString() {
		return "Declaration : " + super.toString();
	}
}
