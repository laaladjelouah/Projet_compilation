package instructions;
import semantic.EnumTag;
import types.Type;
import expressions.Expression;


public class FuncCallInstruction extends Instruction {

	
	public FuncCallInstruction(Expression expr, Instruction instr) {
		super(expr, instr, null);
		
	}	
	
	public FuncCallInstruction(Type t1, Instruction instr) {
		super(t1, instr, EnumTag.FUNCTION1);
		
	}	
	
	public String toString(){
		return "Func Call : " + super.toString();
	}

}
