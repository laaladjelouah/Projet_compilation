package intermediary;

public class InternTree {
	
	protected InternTree left;
	protected InternTree right;
	
	public InternTree(InternTree l, InternTree r) {
		this.left = l;
		this.right = r;
	}
	
	public InternTree(InternTree l) {
		this(l,null);
	}
	
	public InternTree getLeft() {
		return left;
	}
	
	public InternTree getRight() {
		return right;
	}
	
	public InternTree()
	{
		
	}	
}
